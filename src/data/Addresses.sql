DROP TABLE IF EXISTS `Addresses`;
CREATE TABLE IF NOT EXISTS `Addresses` (
  `AddressId` int(11) NOT NULL AUTO_INCREMENT,
  `CompanyId` int(11) NOT NULL,
  `EntityType` varchar(100) CHARACTER SET utf8 DEFAULT NULL,
  `RowId` bigint(20) NOT NULL,
  `CountryId` int(11) NOT NULL,
  `StateId` int(11) NOT NULL,
  `CityId` int(11) NOT NULL,
  `DepartamentId` int(11) NOT NULL,
  `IsDefault` smallint(6) NOT NULL DEFAULT '0',
  `IsChecked` smallint(6) NOT NULL DEFAULT '0',
  `Street` varchar(100) CHARACTER SET utf8 NOT NULL,
  `Number` bigint(20) NOT NULL,
  `Floor` bigint(20) NOT NULL,
  `Departament` varchar(10) CHARACTER SET utf8 DEFAULT NULL,
  `PostalCode` varchar(10) CHARACTER SET utf8 DEFAULT NULL,
  `Section` varchar(10) CHARACTER SET utf8 DEFAULT NULL,
  `BetweenStreet1` varchar(100) CHARACTER SET utf8 DEFAULT NULL,
  `BetweenStreet2` varchar(100) CHARACTER SET utf8 DEFAULT NULL,
  `BetweenStreet3` varchar(100) CHARACTER SET utf8 DEFAULT NULL,
  `BetweenStreet4` varchar(100) CHARACTER SET utf8 DEFAULT NULL,
  `BetweenStreet1Number` bigint(20) DEFAULT NULL,
  `BetweenStreet2Number` bigint(20) DEFAULT NULL,
  `BetweenStreet3Number` bigint(20) DEFAULT NULL,
  `BetweenStreet4Number` bigint(20) DEFAULT NULL,
  `Latitude` double DEFAULT NULL,
  `Longitude` double DEFAULT NULL,
  `Observations` varchar(100) CHARACTER SET utf8 NOT NULL,
  `IsActive` smallint(6) NOT NULL DEFAULT '1',
  `CreatedAt` datetime NOT NULL,
  `UpdatedAt` datetime DEFAULT NULL,
  `DeletedAt` datetime DEFAULT NULL,
  `CreatedBy` varchar(50) CHARACTER SET utf8 NOT NULL,
  `UpdatedBy` varchar(50) CHARACTER SET utf8 DEFAULT NULL,
  `DeletedBy` varchar(50) CHARACTER SET utf8 DEFAULT NULL,
  PRIMARY KEY (`AddressId`),
  KEY `FK_Addresses_CompanyId` (`CompanyId`),
  KEY `FK_Addresses_CountryId` (`CountryId`),
  KEY `FK_Addresses_StateId` (`StateId`),
  KEY `FK_Addresses_CityId` (`CityId`),
  KEY `FK_Addresses_DepartamentId` (`DepartamentId`),
  CONSTRAINT `FK_Addresses_CityId` FOREIGN KEY (`CityId`) REFERENCES `Cities` (`CityId`),
  CONSTRAINT `FK_Addresses_CompanyId` FOREIGN KEY (`CompanyId`) REFERENCES `Companies` (`CompanyId`),
  CONSTRAINT `FK_Addresses_CountryId` FOREIGN KEY (`CountryId`) REFERENCES `Countries` (`CountryId`),
  CONSTRAINT `FK_Addresses_DepartamentId` FOREIGN KEY (`DepartamentId`) REFERENCES `Departaments` (`DepartamentId`),
  CONSTRAINT `FK_Addresses_StateId` FOREIGN KEY (`StateId`) REFERENCES `States` (`StateId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
