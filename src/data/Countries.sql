DROP TABLE IF EXISTS `Countries`;
CREATE TABLE IF NOT EXISTS `Countries` (
  `CountryId` int(11) NOT NULL,
  `CurrencyId` int(11) NOT NULL,
  `Description` varchar(100) CHARACTER SET utf8 NOT NULL,
  `PhoneAreaCode` varchar(3) CHARACTER SET utf8 NOT NULL,
  `Iso_3166_numeric_1` varchar(3) CHARACTER SET utf8 NOT NULL,
  `Iso_3166_alpha_2` varchar(2) CHARACTER SET utf8 NOT NULL,
  `Iso_3166_alpha_3` varchar(3) CHARACTER SET utf8 NOT NULL,
  `IdentityIndividual` varchar(20) CHARACTER SET utf8 NOT NULL,
  `IdentityLegal` varchar(20) CHARACTER SET utf8 NOT NULL,
  `IdentityOther` varchar(20) CHARACTER SET utf8 NOT NULL,
  `IdentityCode` smallint(6) NOT NULL,
  `CountriesImage` varchar(100) CHARACTER SET utf8 DEFAULT NULL,
  `IsActive` smallint(6) NOT NULL DEFAULT '1',
  `CreatedAt` datetime NOT NULL,
  `UpdatedAt` datetime DEFAULT NULL,
  `DeletedAt` datetime DEFAULT NULL,
  `CreatedBy` varchar(50) CHARACTER SET utf8 NOT NULL,
  `UpdatedBy` varchar(50) CHARACTER SET utf8 DEFAULT NULL,
  `DeletedBy` varchar(50) CHARACTER SET utf8 DEFAULT NULL,
  PRIMARY KEY (`CountryId`),
  UNIQUE KEY `IX_Countries_PhoneAreaCode` (`PhoneAreaCode`),
  UNIQUE KEY `IX_Countries_Iso_1` (`Iso_3166_numeric_1`),
  UNIQUE KEY `IX_Countries_Iso_2` (`Iso_3166_alpha_2`),
  UNIQUE KEY `IX_Countries_Iso_3` (`Iso_3166_alpha_3`),
  UNIQUE KEY `IX_Countries_IdentityIndividual` (`IdentityIndividual`),
  UNIQUE KEY `IX_Countries_IdentityLegal` (`IdentityLegal`),
  UNIQUE KEY `IX_Countries_IdentityOther` (`IdentityOther`),
  UNIQUE KEY `IX_Countries_IdentityCode` (`IdentityCode`),
  KEY `FK_Countries_CurrencyId` (`CurrencyId`),
  CONSTRAINT `FK_Countries_CurrencyId` FOREIGN KEY (`CurrencyId`) REFERENCES `Currencies` (`CurrencyId`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
